    int iData = 22222; // 32-bit, -2^31 - (2^31-1)
    long lData = 99999; // 64-bit, -2^63 - (2^63-1)
    float fData = 234.5f; // 32-bit
    double dData = 1233.5f; // 64-bit
    boolean blData = true; // value = true or false
    char cData = 's'; //16-bit. one character

      // Reference Datatypes
